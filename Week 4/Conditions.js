// Simple program to check if a number is positive, negative, or zero

let number = -0; // Ask the user to input a number

// Check the condition using if-else
if (number > 0) {
    console.log("The number is positive.");
} else if (number < 0) {
    console.log("The number is negative.");
} else {
    console.log("The number is zero.");
}
